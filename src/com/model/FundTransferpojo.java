package com.model;

public class FundTransferpojo {
	
	private String saccno;
	private String baccno;
	private String amount;
	private String tdt;
	public String getSaccno() {
		return saccno;
	}
	public void setSaccno(String saccno) {
		this.saccno = saccno;
	}
	public String getBaccno() {
		return baccno;
	}
	public void setBaccno(String baccno) {
		this.baccno = baccno;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getTdt() {
		return tdt;
	}
	public void setTdt(String tdt) {
		this.tdt = tdt;
	}

}

